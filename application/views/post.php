				<p>
					Dates back to May 19, 1980, when a group of prominent business leaders in Indonesia set up the Prasetiya Mulya Foundation (YPM) as a commitment to give real contributions to the nation. The words of Prasetiya Mulya is the Sanskrit words for noble pledge; the name that was given by the President of Republic of Indonesia.
				</p>
				<img src="images/photo2.png" height="" width="" />
				<p>
					With a mission to develop professionalism in Indonesia business management practices that anchored to the sound management knowledge and skill, Prasetiya Mulya Foundation established the Management Development Centre in 1982, which later became the Prasetiya Mulya  Institute of Management (IMPM) ; a not for profit organization to provide management education and training   Beside executive management courses and development program for small and medium enterprises, Prasetiya Mulya Institute of Management also initiated the first MBA program in Indonesia.
				</p>
				<p>
					In 1993, the Prasetiya Mulya Institute of Management which offer the MBA program was renamed to the Sekolah Tinggi Manajemen Prasetiya Mulya (Prasetiya Mulya School of Management), in compliance with new regulations of Indonesia Ministry of Education. These new regulations also required the replacement of Master of Business Administration (MBA) degree with a Magister Management (MM). Meanwhile, the executive learning programs which not offer the academic degree still be managed under Lembaga Manajemen Prasetiya Mulya (Prasetiya Mulya Institute of Management). In 2000, with an objective to promote the existence of business school in Indonesia, we decided to use Prasetiya Mulya Business School as our corporate brand. In order to strengthen the brand of Prasetiya Mulya Business School, we are offering our services in business management education either for academic degree or executive programs under single brand.
				</p>
				<p>
					The enhancement of our services in academic programs as well as in the executive programs, the escalation of quality requirement in both programs, the increasing flexibility needed to cope with the business dynamics and the burgeoning demand of contextualization and customized solution in developing business people certainly require full hearted people, a focus management team and specific competencies to be developed further. All of these things are necessitated to improve our services, to enhance the impact of the solution and to maintain our commitment in developing human asset value of the business.  This was what we have done since 2009 to enhance our services to Indonesia business world in non academic development programs. 
				</p>
